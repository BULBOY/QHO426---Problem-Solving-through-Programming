print("Towards which direction should I paint (up, down, left or right)?\n")
input_command = input()

if (input_command == "up"):
    print("\n\nI am painting in the upward direction!")
elif(input_command == "down"):
    print("\n\nI am painting in the downward direction!")
elif(input_command == "left"):
    print("\n\nI am painting in the leftward direction!")
else:
    print("\n\nI am painting in the rightward direction!")
                
